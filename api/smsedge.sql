 -- SP
DROP PROCEDURE if exists sp_move_bunch_logs;
DELIMITER $$
CREATE PROCEDURE sp_move_bunch_logs(in start_date char(10))
BEGIN
	DECLARE exit handler FOR SQLEXCEPTION, SQLWARNING
	BEGIN
		ROLLBACK;
		RESIGNAL; -- bubbles the error up
	END;

	START TRANSACTION;

	DROP TABLE if exists temp_logs;
	CREATE TEMPORARY TABLE temp_logs (
		log_id bigint unsigned primary key,
		usr_id int unsigned not null,
		num_id int unsigned not null,
		log_message varchar (16350),
		log_success tinyint unsigned not null,
		log_created datetime default current_timestamp,
		log_created_aggregated char(10)
	);
	truncate table temp_logs; -- no need in production

	insert into temp_logs
		select *, DATE_FORMAT(send_log.log_created, '%Y-%m-%d')
		from send_log
		where log_created > start_date;

	truncate table send_log_aggregated; -- for development only
	insert ignore into send_log_aggregated
		select *
		from temp_logs;

	delete from send_log
	where log_id in (
		select log_id from temp_logs where temp_logs.log_created > start_date
	);
	COMMIT;
END $$
DELIMITER ;

set @past_date := '2017-01-01';
