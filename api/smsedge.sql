DROP DATABASE if exists smsedge;
CREATE DATABASE if not exists smsedge character
	set utf8mb4 collate utf8mb4_unicode_ci;
use smsedge;

CREATE TABLE users (
	usr_id int unsigned auto_increment primary key,
	usr_name varchar(60) not null,
	usr_active tinyint unsigned not null,
	usr_created datetime default current_timestamp
);
CREATE TABLE countries (
	cnt_id int unsigned auto_increment primary key,
	cnt_code char(2) not null,
	cnt_title varchar(60) not null,
	cnt_created datetime default current_timestamp,

	index cnt_code_idx (cnt_code)
);
CREATE TABLE numbers (
	num_id int unsigned auto_increment primary key,
	cnt_id int unsigned not null,
	num_number varchar(60) not null,
	num_created datetime default current_timestamp,

	index cnt_id_idx (cnt_id),
	foreign key fk_cnt_id (cnt_id) references countries (cnt_id)
		on delete restrict -- visually reminds NO ACTION
		on update cascade,

	index num_number_idx (num_number)
);
CREATE TABLE send_log (
	log_id bigint unsigned auto_increment primary key,
	usr_id int unsigned not null,
	num_id int unsigned not null,
	log_message varchar (16350),
	log_success tinyint unsigned not null,
	log_created datetime default current_timestamp,

	index usr_id_idx (usr_id),
	foreign key fk_usr_id (usr_id) references users (usr_id)
		on delete restrict -- visually reminds NO ACTION
		on update cascade,
	index num_id_idx (num_id),
	foreign key fk_num_id (num_id) references numbers (num_id)
		on delete restrict -- visually reminds NO ACTION
		on update cascade,

	index log_created_idx (log_created)
);

CREATE TABLE send_log_aggregated (
	log_id bigint unsigned primary key,
	usr_id int unsigned not null,
	num_id int unsigned not null,
	log_message varchar (16350),
	log_success tinyint unsigned not null,
	log_created datetime default current_timestamp,
	log_created_aggregated char(10),

	index usr_id_idx (usr_id),
	foreign key fk_aggr_usr_id (usr_id) references users (usr_id)
		on delete restrict -- visually reminds NO ACTION
		on update cascade,
	index num_id_idx (num_id),
	foreign key fk_aggr_num_id (num_id) references numbers (num_id)
		on delete restrict -- visually reminds NO ACTION
		on update cascade,

	index log_created_idx (log_created)
);

